/// Apple风格动画图标构建器
/// 
/// 提供常用的图标动画效果
class AppleAnimatedIcon extends StatelessWidget {
  final IconData icon;
  final double size;
  final Color? color;
  final Duration duration;
  final Curve curve;
  final bool rotate;
  final bool pulse;
  final bool bounce;

  const AppleAnimatedIcon({
    super.key,
    required this.icon,
    this.size = 24.0,
    this.color,
    this.duration = const Duration(milliseconds: 300),
    this.curve = Curves.easeInOut,
    this.rotate = false,
    this.pulse = false,
    this.bounce = false,
  });

  @override
  Widget build(BuildContext context) {
    Widget iconWidget = Icon(icon, size: size, color: color);

    if (rotate) {
      return _RotatingIcon(
        duration: duration,
        curve: curve,
        child: iconWidget,
      );
    }

    if (pulse) {
      return _PulsingIcon(
        duration: duration,
        curve: curve,
        child: iconWidget,
      );
    }

    if (bounce) {
      return _BouncingIcon(
        duration: duration,
        curve: curve,
        child: iconWidget,
      );
    }

    return iconWidget;
  }
}

class _RotatingIcon extends StatefulWidget {
  final Widget child;
  final Duration duration;
  final Curve curve;

  const _RotatingIcon({
    required this.child,
    required this.duration,
    required this.curve,
  });

  @override
  State<_RotatingIcon> createState() => _RotatingIconState();
}

class _RotatingIconState extends State<_RotatingIcon>
    with SingleTickerProviderStateMixin {
  late AnimationController _controller;

  @override
  void initState() {
    super.initState();
    _controller = AnimationController(duration: widget.duration, vsync: this)
      ..repeat();
  }

  @override
  void dispose() {
    _controller.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return RotationTransition(
      turns: _controller,
      child: widget.child,
    );
  }
}

class _PulsingIcon extends StatefulWidget {
  final Widget child;
  final Duration duration;
  final Curve curve;

  const _PulsingIcon({
    required this.child,
    required this.duration,
    required this.curve,
  });

  @override
  State<_PulsingIcon> createState() => _PulsingIconState();
}

class _PulsingIconState extends State<_PulsingIcon>
    with SingleTickerProviderStateMixin {
  late AnimationController _controller;
  late Animation<double> _animation;

  @override
  void initState() {
    super.initState();
    _controller = AnimationController(duration: widget.duration, vsync: this)
      ..repeat(reverse: true);
    _animation = Tween<double>(begin: 0.8, end: 1.0).animate(
      CurvedAnimation(parent: _controller, curve: widget.curve),
    );
  }

  @override
  void dispose() {
    _controller.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return ScaleTransition(
      scale: _animation,
      child: widget.child,
    );
  }
}

class _BouncingIcon extends StatefulWidget {
  final Widget child;
  final Duration duration;
  final Curve curve;

  const _BouncingIcon({
    required this.child,
    required this.duration,
    required this.curve,
  });

  @override
  State<_BouncingIcon> createState() => _BouncingIconState();
}

class _BouncingIconState extends State<_BouncingIcon>
    with SingleTickerProviderStateMixin {
  late AnimationController _controller;
  late Animation<double> _animation;

  @override
  void initState() {
    super.initState();
    _controller = AnimationController(duration: widget.duration, vsync: this)
      ..repeat(reverse: true);
    _animation = Tween<double>(begin: 0.0, end: -8.0).animate(
      CurvedAnimation(parent: _controller, curve: Curves.bounceOut),
    );
  }

  @override
  void dispose() {
    _controller.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return AnimatedBuilder(
      animation: _animation,
      builder: (context, child) {
        return Transform.translate(
          offset: Offset(0, _animation.value),
          child: child,
        );
      },
      child: widget.child,
    );
  }
}
